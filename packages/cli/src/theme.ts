export type ThemeColors = {
    primary: string;
    planMode: string;
    selection: string;
    thinking: string;
    success: string;
    error: string;
    info: string;
    background: string;
    surface: string;
    dialogSurface: string;
    thinkingBorder: string;
    dimSeparator: string;
};

export type Theme = {
    name: string;
    colors: ThemeColors;
};

type ThemePalette = {
    accent: string;
    accent2: string;
    selection?: string;
    success: string;
    error: string;
    info: string;
    background: string;
    surface: string;
    dialogSurface: string;
    border: string;
    separator: string;
};

function theme(name: string, p: ThemePalette): Theme {
    return {
        name,
        colors: {
            primary: p.accent,
            planMode: p.accent2,
            selection: p.selection ?? p.accent,
            thinking: p.accent2,
            success: p.success,
            error: p.error,
            info: p.info,
            background: p.background,
            surface: p.surface,
            dialogSurface: p.dialogSurface,
            thinkingBorder: p.border,
            dimSeparator: p.separator,
        },
    };
}

